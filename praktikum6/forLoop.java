public class forLoop{
    public static void main(String[] args){
        int n = 9; //memberikan sebuah nilai ke n

        for (int i = 0; i <= n; i++){ 
        /*untuk mengulang nilai i sampai memenuhi 
        kondisi yang telah ditentukan*/
            System.out.print(i + " "); //mencetak nilai i
        }
    }
}